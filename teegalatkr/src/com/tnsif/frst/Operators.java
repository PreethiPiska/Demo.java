package com.tnsif.frst;

class Operators {
	  public static void main(String[] args) {
	    int a = 10, b = 5;
	    System.out.println("a + b = " + (a + b));
	    System.out.println("a - b = " + (a - b));
	    System.out.println("a * b = " + (a * b));
	    System.out.println("a / b = " + (a / b));
	    System.out.println("a % b = " + (a % b));
	  }
	}